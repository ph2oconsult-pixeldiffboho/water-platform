# ── Step 6: Structured residual risks ────────────────────────────────────────

def _build_residual_risks(
    pathway: UpgradePathway,
    feasibility: FeasibilityReport,
    flow_ratio: float,
) -> List[ResidualRisk]:
    """Build a complete, categorised residual risk list."""
    risks: List[ResidualRisk] = []
    tech_in_stack = {s.technology for s in pathway.stages}
    ct_types      = {c.constraint_type for c in pathway.constraints}

    # Always: hydraulic peaks
    risks.append(ResidualRisk(
        category="Hydraulic",
        risk=(
            f"Extreme wet weather flows (> 3× DWA) may still require upstream sewer attenuation "
            f"or storm storage even after the process upgrade stack is implemented. "
            f"{'(Current flow ratio is ' + str(round(flow_ratio,1)) + '× DWA — hydraulic attenuation is already required.)' if flow_ratio >= 3.0 else ''}"
        ).strip(),
        urgency="Planning horizon" if flow_ratio < 2.0 else "Immediate",
    ))

    # Chemical dependency
    if feasibility.chemical_dependency in ("Medium", "High"):
        risks.append(ResidualRisk(
            category="Chemical",
            risk=(
                "Chemical supply dependency persists after upgrade. "
                + ("Methanol supply disruption halts tertiary denitrification. "
                   if TI_DENFILTER in tech_in_stack else "")
                + "Dual-supply agreements and on-site storage should be established before commissioning."
            ),
            urgency="Planning horizon",
        ))

    # TN carbon limitation
    if (CT_TN_POLISH in ct_types or CT_BIOLOGICAL in ct_types) and TI_DENFILTER not in tech_in_stack:
        risks.append(ResidualRisk(
            category="Chemical",
            risk=(
                "TN compliance may require external carbon dosing (methanol or acetate) if COD/N < 4 "
                "after internal recycle optimisation. Quantify COD/N ratio and assess carbon supplementation "
                "requirement before designing final TN strategy."
            ),
            urgency="Planning horizon",
        ))

    # Operational complexity
    if len(pathway.stages) >= 3 or feasibility.operational_complexity in ("High", "Medium"):
        risks.append(ResidualRisk(
            category="Operational",
            risk=(
                f"A {len(pathway.stages)}-stage upgrade increases operational complexity. "
                "Staged commissioning with effluent monitoring between each stage is essential. "
                "Operator training and specialist OEM support agreements should be established "
                "before commissioning."
            ),
            urgency="Planning horizon",
        ))

    # Biofilm establishment
    if any(t in tech_in_stack for t in (TI_IFAS, TI_HYBAS, TI_MBBR, TI_MABR)):
        risks.append(ResidualRisk(
            category="Operational",
            risk=(
                "Biofilm establishment takes 4–8 weeks after media installation. "
                "Effluent monitoring triggers should be adjusted during the establishment period. "
                "WAS rate and aeration control strategy must be recalibrated once biofilm is active."
            ),
            urgency="Monitoring",
        ))

    # Future upgrade trigger
    risks.append(ResidualRisk(
        category="Future trigger",
        risk=(
            "If flow growth or licence tightening occurs beyond the intensified envelope, "
            "the next upgrade trigger is: "
            + ("civil expansion (new secondary clarifier or bioreactor volume) "
               if CT_SETTLING in ct_types else
               "expansion of biological capacity (additional media or module count) ")
            + "or catchment-level I/I reduction programme."
        ),
        urgency="Planning horizon",
    ))

    return risks


# ── Step 7: Narrative enforcement ────────────────────────────────────────────

def _executive_summary(pathway: UpgradePathway, feasibility: FeasibilityReport) -> str:
    """Generate a cause→mechanism→solution executive summary."""
    pc    = pathway.primary_constraint
    state = pathway.system_state
    prox  = pathway.proximity_pct
    n_stages = len(pathway.stages)
    conf  = feasibility.adjusted_confidence

    # Primary constraint in cause → mechanism → solution language
    cause_map = {
        "hydraulic_limitation":             "hydraulically constrained under peak wet weather conditions",
        "wet_weather_peak":                 "exposed to peak wet weather flows that exceed design capacity",
        "settling_limitation":              "settling-limited, with solids carry-over risk under rising load",
        "nitrification_limitation":         "nitrification-limited due to insufficient effective sludge age",
        "tn_polishing_limitation":          "TN-limited due to carbon-available denitrification deficit",
        "tp_polishing_limitation":          "TP-limited, requiring tertiary chemical polishing",
        "biological_performance_limitation":"biologically underperforming relative to licence targets",
        "membrane_performance_limitation":  "membrane performance-limited by biomass quality and fouling",
    }
    cause = cause_map.get(pc.constraint_type,
                          pc.label.lower() + " limited")

    # Stage description
    if n_stages == 1:
        stage_desc = f"a targeted {pathway.stages[0].tech_display.split('(')[0].strip()} installation"
    elif n_stages == 2:
        stage_desc = (f"{pathway.stages[0].tech_display.split('(')[0].strip()} "
                      f"followed by {pathway.stages[1].tech_display.split('(')[0].strip()}")
    else:
        stage_desc = (f"a {n_stages}-stage intensification sequence "
                      f"({', '.join(s.tech_display.split('(')[0].strip() for s in pathway.stages[:3])})")

    summary_p1 = (
        f"The {pathway.plant_type} plant is currently {cause} "
        f"at {prox:.0f}% of design capacity ({state}). "
        f"The recommended upgrade pathway is {stage_desc}, addressing the primary constraint "
        f"through {pathway.stages[0].mechanism_label.lower() if pathway.stages else 'process intensification'} "
        f"without requiring major civil expansion."
    )

    summary_p2 = (
        f"Feasibility is assessed as {feasibility.overall_feasibility} "
        f"with {conf} confidence. "
        + (f"The primary feasibility consideration is {feasibility.supply_chain_risk.lower()} "
           f"supply chain risk and {feasibility.chemical_dependency.lower()} chemical dependency. "
           if feasibility.chemical_dependency != "None" else
           f"The stack is operationally straightforward with low supply chain dependency. ")
        + ("A lower-risk alternative is available — see Alternative Pathways."
           if feasibility.lower_risk_alternative else
           "No significant feasibility concerns are identified with the recommended pathway.")
    )

    return f"{summary_p1}\n\n{summary_p2}"


def _why_stack_works(pathway: UpgradePathway) -> str:
    """Generate cause → mechanism → solution linkage for the full stack."""
    if not pathway.stages:
        return "Insufficient data to generate stack rationale."
    parts = [
        "This upgrade pathway is sequenced to address the highest-priority constraint first, "
        "with each stage unlocking the next and avoiding functional redundancy.\n"
    ]
    for s in pathway.stages:
        parts.append(
            f"Stage {s.stage_number} — {s.tech_display.split('(')[0].strip()}: "
            f"{s.purpose} {s.engineering_basis[:120]}".rstrip() + "."
        )
    parts.append(
        "\nTechnologies have been selected to exhaust process intensification options "
        "before recommending civil expansion, consistent with the principle that intensification "
        "should precede volume addition in brownfield upgrade planning."
    )
    return "\n".join(parts)


def _feasibility_narrative(feasibility: FeasibilityReport) -> str:
    """Refined feasibility paragraph using consultant language."""
    dim_map = {d.dimension: d.score for d in feasibility.dimensions}
    high_risks = [d.dimension for d in feasibility.dimensions
                  if d.score in (FS_MEDIUM, FS_LOW)]

    if not high_risks:
        return (
            "This solution is technically robust with low operational dependency and a broad supply base. "
            "All six feasibility dimensions score positively. "
            f"{feasibility.feasibility_summary}"
        )
    dims_str = ", ".join(high_risks[:3])
    return (
        f"Feasibility is {feasibility.overall_feasibility.upper()} overall, with moderate attention "
        f"required on: {dims_str}. "
        + ("This solution introduces supply chain dependency on specialist materials — "
           "pre-qualification of suppliers and contingency stock management are recommended. "
           if feasibility.supply_chain_risk == "High" else "")
        + ("Operational complexity increases due to chemical dosing and process control requirements. "
           if feasibility.chemical_dependency in ("Medium", "High") else "")
        + (f"Mitigations: {'; '.join(feasibility.key_mitigations[:2])}."
           if feasibility.key_mitigations else "")
    )


# ── Step 8: Consistency check ─────────────────────────────────────────────────

def _consistency_check(
    pathway: UpgradePathway,
    feasibility: FeasibilityReport,
    alternatives: List[VerifiedAlternative],
) -> List[str]:
    """Detect contradictions between outputs."""
    flags: List[str]  = []
    tech_in_stack     = {s.technology for s in pathway.stages}
    ct_types          = {c.constraint_type for c in pathway.constraints}

    # Flag: CoMag recommended but hydraulic attenuation note says can't substitute
    if TI_COMAG in tech_in_stack and feasibility.supply_chain_risk == "High":
        eng_notes_text = " ".join(feasibility.key_risks)
        if "substitute" in eng_notes_text.lower() and "attenuation" in eng_notes_text.lower():
            pass  # CoMag IS a form of attenuation — no contradiction

    # Flag: settling recommended but no settling constraint identified
    settling_tech = {TI_INDENSE, TI_MIGINDENSE, TI_BIOMAG, TI_MEMDENSE}
    if settling_tech & tech_in_stack and CT_SETTLING not in ct_types and CT_MEMBRANE not in ct_types:
        flags.append(
            "Consistency: settling technology in stack but no settling constraint identified in constraint list. "
            "Verify that settling limitation was correctly classified from failure modes."
        )

    # Flag: DNF in stack but nitrification limitation unresolved
    if TI_DENFILTER in tech_in_stack:
        nit_resolved = any(t in tech_in_stack for t in (TI_IFAS, TI_HYBAS, TI_MBBR, TI_MABR, TI_MIGINDENSE))
        if not nit_resolved and CT_NITRIFICATION in ct_types:
            flags.append(
                "Consistency: Denitrification filter in stack but nitrification constraint is unresolved "
                "and no biofilm technology is present. DNF requires stable nitrification upstream."
            )

    # Flag: No alternatives if multi-constraint
    if pathway.multi_constraint and len(alternatives) < 2:
        flags.append(
            "Consistency: Multi-constraint plant with fewer than 2 alternatives — additional "
            "alternative pathway should be presented."
        )

    return flags


# ── Step 9: Final output assembly ─────────────────────────────────────────────

def build_credible_output(
    pathway: UpgradePathway,
    feasibility: FeasibilityReport,
    plant_context: Optional[Dict] = None,
) -> CredibleOutput:
    """
    Build the final credible, client-presentable output.

    Parameters
    ----------
    pathway : UpgradePathway
        Output of build_upgrade_pathway().

    feasibility : FeasibilityReport
        Output of assess_feasibility().

    plant_context : dict, optional
        Same dict passed to prior layers:
          flow_ratio, is_sbr, is_mbr, plant_size_mld, location_type.

    Returns
    -------
    CredibleOutput
        Does NOT modify pathway or feasibility.
    """
    ctx        = plant_context or {}
    flow_ratio = ctx.get("flow_ratio", 1.0) or 1.0

    all_notes:    List[CredibilityNote]       = []
    all_gen_alts: List[VerifiedAlternative]   = []

    # ── Step 1: Rule consistency ───────────────────────────────────────────────
    rule_notes, rule_alts = _check_rule_consistency(pathway, ctx)
    all_notes.extend(rule_notes)
    all_gen_alts.extend(rule_alts)

    # ── Step 2: Completeness ───────────────────────────────────────────────────
    comp_notes, comp_alts = _check_completeness(pathway)
    all_notes.extend(comp_notes)
    all_gen_alts.extend(comp_alts)

    # ── Step 3: Ensure ≥2 alternatives ────────────────────────────────────────
    verified_alts, alt_notes = _ensure_minimum_alternatives(
        pathway, all_gen_alts, feasibility)
    all_notes.extend(alt_notes)

    # ── Step 4: Compatibility flags ────────────────────────────────────────────
    compat_flags = _check_compatibility(pathway, ctx)

    # ── Step 5: Ranking clarification (always added) ──────────────────────────
    # (constant string, no computation needed)

    # ── Step 6: Structured residual risks ─────────────────────────────────────
    residual_risks = _build_residual_risks(pathway, feasibility, flow_ratio)

    # ── Step 7: Narrative ─────────────────────────────────────────────────────
    exec_sum    = _executive_summary(pathway, feasibility)
    why_works   = _why_stack_works(pathway)
    feas_narr   = _feasibility_narrative(feasibility)

    # ── Step 8: Consistency check ──────────────────────────────────────────────
    consistency = _consistency_check(pathway, feasibility, verified_alts)

    # ── Final confidence and ready flag ───────────────────────────────────────
    final_conf = feasibility.adjusted_confidence
    warnings   = [n for n in all_notes if n.severity in ("Warning", "Correction")]
    ready      = len(warnings) == 0 and len(consistency) == 0

    conf_stmt = (
        f"Recommendation confidence: {final_conf}. "
        f"{feasibility.confidence_reason} "
        + (f"Note: {len(warnings)} credibility warning(s) require review before client presentation. "
           if warnings else "No credibility issues identified — output is ready for client presentation.")
    )

    return CredibleOutput(
        pathway              = pathway,
        feasibility          = feasibility,
        recommended_stages   = list(pathway.stages),
        alternatives         = verified_alts,
        residual_risks       = residual_risks,
        credibility_notes    = all_notes,
        consistency_flags    = consistency,
        compatibility_flags  = compat_flags,
        executive_summary    = exec_sum,
        why_stack_works      = why_works,
        ranking_clarification= _RANKING_CLARIFICATION,
        feasibility_narrative= feas_narr,
        confidence_statement = conf_stmt,
        final_confidence     = final_conf,
        ready_for_client     = ready,
    )
