# ── Result dataclasses ─────────────────────────────────────────────────────────

@dataclass
class CredibilityNote:
    """A flag or note added by the credibility layer."""
    category:  str   # "Rule Consistency" / "Completeness" / "Compatibility" / "Language"
    severity:  str   # "Info" / "Warning" / "Correction"
    message:   str
    applied:   bool  # True if the note required a change to the output


@dataclass
class VerifiedAlternative:
    """An alternative pathway that has passed credibility checks."""
    label:          str
    mechanism_label:str
    stages:         List[str]    # tech display names in order
    rationale:      str
    when_preferred: str
    capex_class:    str
    is_generated:   bool = False   # True if added by credibility layer (not from stack_generator)


@dataclass
class ResidualRisk:
    """A single residual risk with category and urgency."""
    category:  str   # "Hydraulic" / "Chemical" / "Operational" / "Future trigger"
    risk:      str
    urgency:   str   # "Immediate" / "Planning horizon" / "Monitoring"


@dataclass
class CredibleOutput:
    """
    Complete, validated, client-presentable engineering recommendation.

    This is the final synthesis of UpgradePathway + FeasibilityReport
    after credibility checks have been applied.
    """
    # ── Source inputs (unchanged) ──────────────────────────────────────────────
    pathway:            UpgradePathway
    feasibility:        FeasibilityReport

    # ── Validated content ──────────────────────────────────────────────────────
    # Recommended stack (verified — same stages as pathway.stages)
    recommended_stages: List[PathwayStage]

    # Alternatives (pathway originals + any generated by credibility layer)
    alternatives:       List[VerifiedAlternative]

    # Structured residual risks
    residual_risks:     List[ResidualRisk]

    # ── Credibility checks ─────────────────────────────────────────────────────
    credibility_notes:  List[CredibilityNote]
    consistency_flags:  List[str]    # contradictions detected
    compatibility_flags:List[str]    # technology compatibility warnings

    # ── Narrative sections ─────────────────────────────────────────────────────
    executive_summary:      str    # 1–2 paragraph concept design recommendation
    why_stack_works:        str    # cause → mechanism → solution linkage
    ranking_clarification:  str    # standard ranking-vs-stack note
    feasibility_narrative:  str    # refined feasibility paragraph
    confidence_statement:   str    # final confidence with reason

    # ── Final status ───────────────────────────────────────────────────────────
    final_confidence:   str   # High / Medium / Low
    ready_for_client:   bool  # True if no Warning/Correction credibility notes remain


# ── Step 1: Rule consistency check ────────────────────────────────────────────

def _check_rule_consistency(
    pathway: UpgradePathway,
    plant_context: Dict,
) -> Tuple[List[CredibilityNote], List[VerifiedAlternative]]:
    """
    Enforce engineering rules that must appear in outputs.
    Returns (notes, generated_alternatives).
    """
    notes: List[CredibilityNote] = []
    gen_alts: List[VerifiedAlternative] = []
    flow_ratio   = plant_context.get("flow_ratio", 1.0) or 1.0
    ct_types     = {c.constraint_type for c in pathway.constraints}
    tech_in_stack= {s.technology for s in pathway.stages}

    # Rule: hydraulic attenuation required above 3× DWA
    # Fix 4: fire when flow_ratio >= 3.0 regardless of whether CoMag is in stack.
    # If CoMag IS present: emit Info note (not Warning) — CoMag provides relief but
    # does not eliminate the need for upstream attenuation at extreme peaks.
    # If CoMag is absent: emit Warning + generate EQ basin alternative (existing logic).
    _has_attenuation = TI_EQ_BASIN in tech_in_stack or TI_STORM_STORE in tech_in_stack
    _has_comag       = TI_COMAG in tech_in_stack or TI_BIOMAG in tech_in_stack

    if flow_ratio >= 3.0 and not _has_attenuation and _has_comag:
        # CoMag is present but extreme peaks still need complementary attenuation
        notes.append(CredibilityNote(
            category="Rule Consistency",
            severity="Info",
            message=(
                f"Peak flows exceed {flow_ratio:.1f}\u00d7 ADWF (above the \u223c3\u00d7 threshold). "
                "While high-rate clarification (CoMag / BioMag) provides significant hydraulic relief, "
                "extreme events may still require upstream attenuation (EQ basin, storm storage, "
                "or I/I reduction programme). Include attenuation infrastructure in the 15-year "
                "asset management plan."
            ),
            applied=False,   # informational — does not modify stack
        ))

    if flow_ratio >= 3.0 and TI_EQ_BASIN not in tech_in_stack and TI_COMAG not in tech_in_stack:
        notes.append(CredibilityNote(
            category="Rule Consistency",
            severity="Warning",
            message=(
                f"Flow ratio {flow_ratio:.1f}× DWA exceeds the 3× threshold where hydraulic "
                "attenuation is required as the primary intervention. "
                "EQ basin or storm storage should appear in the primary stack or alternatives."
            ),
            applied=True,
        ))
        gen_alts.append(VerifiedAlternative(
            label="Option — Hydraulic attenuation first (EQ basin / storm storage)",
            mechanism_label="Hydraulic expansion / attenuation storage",
            stages=["Equalisation / flow balancing basin", "Storm storage / attenuation infrastructure"],
            rationale=(
                f"At {flow_ratio:.1f}× DWA, extreme peak wet weather flows cannot be resolved by "
                "process intensification alone. An equalisation basin targeting ≤ 2× DWA at the "
                "secondary inlet is the most reliable hydraulic mitigation before any biological "
                "upgrade is commissioned."
            ),
            when_preferred=(
                "When overflow, bypass, or treatment disruption occurs during peak wet weather events. "
                "Must be addressed before biological upgrades are sized or commissioned."
            ),
            capex_class="High",
            is_generated=True,
        ))

    # Rule: TN-only case must have Bardenpho reachable
    if (CT_TN_POLISH in ct_types or CT_BIOLOGICAL in ct_types) and CT_NITRIFICATION not in ct_types:
        bardenpho_present = (TI_BARDENPHO in tech_in_stack or TI_RECYCLE_OPT in tech_in_stack)
        if not bardenpho_present:
            notes.append(CredibilityNote(
                category="Completeness",
                severity="Warning",
                message=(
                    "TN / biological performance limitation is active but Bardenpho zone "
                    "optimisation or recycle ratio optimisation does not appear in the stack or alternatives. "
                    "Process optimisation is the primary mechanism for carbon-limited TN."
                ),
                applied=True,
            ))
            gen_alts.append(VerifiedAlternative(
                label="Option — Process optimisation pathway (Bardenpho / recycle)",
                mechanism_label="Process optimisation",
                stages=["Recycle ratio optimisation", "Bardenpho / process zone optimisation"],
                rationale=(
                    "Where NH4 is controlled and TN remains elevated due to carbon-limited "
                    "denitrification, process zone reconfiguration and recycle optimisation are the "
                    "primary engineering response. Internal recycle R ≈ 2 recovers ~67% of nitrate "
                    "without capital spend. Bardenpho zone optimisation achieves further TN reduction "
                    "with modest civil works."
                ),
                when_preferred=(
                    "When nitrification is performing and the primary issue is NOx reduction. "
                    "Lower CAPEX and complexity than biological expansion."
                ),
                capex_class="Low",
                is_generated=True,
            ))

    # Rule: settling limitation must have at least two pathways (inDENSE and BioMag)
    if CT_SETTLING in ct_types:
        has_selection  = any(t in tech_in_stack for t in (TI_INDENSE, TI_MIGINDENSE, TI_BIOMAG, TI_MEMDENSE))
        has_ballasted  = TI_BIOMAG in tech_in_stack
        if has_selection and not has_ballasted:
            # Check whether BioMag appears in alternatives
            alt_techs = " ".join(a.label for a in pathway.alternatives)
            if "BioMag" not in alt_techs and "biomag" not in alt_techs.lower():
                gen_alts.append(VerifiedAlternative(
                    label="Option — BioMag (ballasted biology) as settling alternative",
                    mechanism_label="Ballasted settling / high-rate clarification",
                    stages=["BioMag® (ballasted MBBR-activated sludge hybrid)"],
                    rationale=(
                        "BioMag combines magnetic ballasted settling with integrated biological treatment. "
                        "Where clarifier loading is critical and MLSS is elevated, BioMag provides more "
                        "robust settling improvement than inDENSE alone by increasing sludge density and "
                        "settling velocity via magnetic microspheres."
                    ),
                    when_preferred=(
                        "When inDENSE alone is insufficient to resolve clarifier loading under peak "
                        "biological load, or where MLSS consistently exceeds 5,500 mg/L."
                    ),
                    capex_class="Medium",
                    is_generated=True,
                ))

    return notes, gen_alts


# ── Step 2: Completeness check ────────────────────────────────────────────────

def _check_completeness(
    pathway: UpgradePathway,
) -> Tuple[List[CredibilityNote], List[VerifiedAlternative]]:
    """Ensure no pathway stops prematurely."""
    notes: List[CredibilityNote] = []
    gen_alts: List[VerifiedAlternative] = []
    ct_types     = {c.constraint_type for c in pathway.constraints}
    tech_in_stack= {s.technology for s in pathway.stages}

    # Nitrification: ensure IFAS vs MABR alternatives exist
    if CT_NITRIFICATION in ct_types:
        biofilm_in_stack = any(t in tech_in_stack for t in (TI_IFAS, TI_HYBAS, TI_MBBR, TI_MABR))
        alt_labels = " ".join(a.label for a in pathway.alternatives).lower()
        has_mabr_alt = "mabr" in alt_labels or "mabr" in " ".join(
            s.technology for s in pathway.stages).lower()
        has_ifas_alt = any(t in tech_in_stack for t in (TI_IFAS, TI_HYBAS, TI_MBBR))

        if biofilm_in_stack and not has_mabr_alt:
            gen_alts.append(VerifiedAlternative(
                label="Option — MABR OxyFAS as aeration-efficient nitrification alternative",
                mechanism_label="Aeration intensification (membrane O₂ delivery)",
                stages=["MABR OxyFAS® (membrane-aerated biofilm reactor)"],
                rationale=(
                    "MABR delivers oxygen directly to the biofilm via hollow-fibre membranes at "
                    "up to 14 kgO₂/kWh — 7–10× more efficient than conventional diffused aeration. "
                    "Where aeration capacity is constrained, MABR provides nitrification intensification "
                    "without blower expansion. NHx <0.1 mg/L is achievable across a wide load range "
                    "(Kawana modelling calibration)."
                ),
                when_preferred=(
                    "When aeration blower capacity is constrained or energy minimisation is a "
                    "strategic priority. Suitable for plants where blower expansion is not feasible."
                ),
                capex_class="Medium",
                is_generated=True,
            ))
        elif not biofilm_in_stack and not has_ifas_alt:
            notes.append(CredibilityNote(
                category="Completeness",
                severity="Info",
                message=(
                    "Nitrification limitation identified but no biofilm retention technology appears "
                    "in the stack. IFAS or MABR should be considered as primary or alternative."
                ),
                applied=False,
            ))

    return notes, gen_alts


# ── Step 3: Ensure minimum 2 alternatives ────────────────────────────────────

def _ensure_minimum_alternatives(
    pathway: UpgradePathway,
    existing_gen: List[VerifiedAlternative],
    feasibility: FeasibilityReport,
) -> Tuple[List[VerifiedAlternative], List[CredibilityNote]]:
    """Ensure at least 2 credible alternatives exist (from any source)."""
    notes: List[CredibilityNote] = []
    # Convert existing pathway.alternatives to VerifiedAlternative
    verified: List[VerifiedAlternative] = []
    for a in pathway.alternatives:
        verified.append(VerifiedAlternative(
            label=a.label, mechanism_label="",
            stages=a.stages, rationale=a.rationale,
            when_preferred=a.when_preferred, capex_class=a.capex_class,
        ))

    # Add feasibility lower-risk alternative if present
    if feasibility.lower_risk_alternative:
        lra = feasibility.lower_risk_alternative
        tech_list = [c.split("→")[-1].strip() for c in lra.stage_changes]
        verified.append(VerifiedAlternative(
            label=lra.label,
            mechanism_label="Reduced-risk configuration",
            stages=tech_list,
            rationale=lra.rationale,
            when_preferred=" ".join(lra.trade_offs[:2]),
            capex_class=lra.capex_impact if lra.capex_impact in ("Low","Medium","High") else "Medium",
            is_generated=True,
        ))

    # Add generated alternatives
    verified.extend(existing_gen)

    # Deduplicate by label
    seen_labels = set()
    deduped = []
    for v in verified:
        if v.label not in seen_labels:
            deduped.append(v)
            seen_labels.add(v.label)

    # If fewer than 2, generate a civil expansion fallback
    if len(deduped) < 2:
        notes.append(CredibilityNote(
            category="Completeness",
            severity="Info",
            message="Fewer than 2 alternatives existed — civil expansion fallback added.",
            applied=True,
        ))
        deduped.append(VerifiedAlternative(
            label="Option — Conventional civil expansion (last resort)",
            mechanism_label="Civil expansion",
            stages=["Secondary clarifier expansion", "New bioreactor volume",
                    "Additional blower / aeration capacity"],
            rationale=(
                "Conventional civil expansion addresses all constraints through volume addition "
                "rather than process efficiency. Should only be pursued after intensification options "
                "are fully evaluated and demonstrably exhausted."
            ),
            when_preferred=(
                "When the intensification envelope is exhausted, performance targets are not achievable "
                "through biological or hydraulic optimisation, or footprint constraints do not apply."
            ),
            capex_class="High",
            is_generated=True,
        ))

    return deduped[:5], notes   # cap at 5 alternatives


# ── Step 4: Technology compatibility flags ────────────────────────────────────

def _check_compatibility(
    pathway: UpgradePathway,
    plant_context: Dict,
) -> List[str]:
    """Return list of compatibility warnings."""
    flags = []
    is_sbr = plant_context.get("is_sbr", False)
    is_mbr = plant_context.get("is_mbr", False)
    tech_in_stack = {s.technology for s in pathway.stages}

    # SBR + CoMag
    if is_sbr and TI_COMAG in tech_in_stack:
        flags.append(
            "SBR + CoMag: CoMag is a continuous-flow high-rate clarifier. Integration with a "
            "batch SBR process requires a dedicated side-stream or bypass configuration — it cannot "
            "be installed inline with SBR decant. A bypass lane or storm-event bypass loop must be "
            "designed. EQ basin may be a simpler primary hydraulic option for SBR plants."
        )

    # MBR + IFAS/MBBR (potential incompatibility)
    if is_mbr and any(t in tech_in_stack for t in (TI_IFAS, TI_MBBR)):
        flags.append(
            "MBR + IFAS/MBBR: Carrier media in an MBR can enter membrane tanks and cause membrane "
            "damage unless dedicated media retention screens are installed upstream of the membrane "
            "tank inlet. This is a manageable engineering constraint but requires explicit design "
            "provision — not a standard IFAS/MBBR installation."
        )

    # CoMag / BioMag magnetite dependency note
    if any(t in tech_in_stack for t in (TI_COMAG, TI_BIOMAG)):
        tech_name = "CoMag" if TI_COMAG in tech_in_stack else "BioMag"
        flags.append(
            f"{tech_name}: Continuous magnetite supply and on-site magnetic recovery system "
            "are required. Recovery efficiency (typically >99.5%) determines whole-of-life cost. "
            "Establish pre-qualified supplier agreements and local stock before commissioning."
        )

    # DNF + high DO carryover risk
    if TI_DENFILTER in tech_in_stack:
        flags.append(
            "Denitrification Filter: DO carryover from secondary treatment must be controlled to "
            "< 0.5 mg/L at the filter inlet. Elevated DO from the biological stage will suppress "
            "denitrification regardless of methanol dose. Assess secondary effluent DO profile "
            "before filter sizing."
        )

    # ── MBR architecture notes (v24Z41) ───────────────────────────────────────
    if is_mbr:
        mbr_report = assess_mbr_applicability(plant_context)
        # Architecture role and fit level
        fit_prefix = {MBR_STRONG: "Strong fit", MBR_MODERATE: "Moderate fit",
                      MBR_WEAK: "Weak fit"}.get(mbr_report.fit_level, "")
        fit_note = (
            f"MBR architecture ({fit_prefix}): {mbr_report.architecture_role} "
            f"{mbr_report.energy_note} "
            f"{mbr_report.operations_note} "
            f"{mbr_report.lifecycle_note}"
        )
        flags.append(fit_note)
        # Existing MBR guidance
        if mbr_report.existing_mbr_note:
            flags.append(mbr_report.existing_mbr_note)
        # Weak fit warning
        if mbr_report.weak_fit_factors:
            flags.append(
                "MBR weak-fit factors for this scenario: "
                + " ".join(mbr_report.weak_fit_factors)
            )
        # MABR differentiation — always include when is_mbr to prevent confusion
        flags.append(mbr_report.mabr_differentiation)
        # Decision tension
        flags.append(mbr_report.decision_tension)

    return flags


